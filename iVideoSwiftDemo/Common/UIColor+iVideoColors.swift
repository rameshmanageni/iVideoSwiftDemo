import UIKit

extension UIColor {

    public class var iVideoDarkGray: UIColor {
        return UIColor(red: 118/255, green: 118/255, blue: 118/255, alpha: 1.0)
    }
    
    public class var iVideoErrorRed: UIColor {
        return UIColor(red: 196/255, green: 31/255, blue: 32/255, alpha: 1.0)
    }
    
    public class var iVideoSuccessGreen: UIColor {
        return UIColor(red: 54/255, green: 102/255, blue: 41/255, alpha: 1.0)
    }
}
